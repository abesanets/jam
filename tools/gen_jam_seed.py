"""Генерация users.jam / orders.jam — фирменные файлы данных (тот же формат: UTF-8 + XOR+hex строки)."""
import os

KEY = b"J4M_s3cr3t_k3y!"


def xor_hex(data: bytes) -> str:
    out = bytearray(len(data))
    for i, b in enumerate(data):
        out[i] = b ^ KEY[i % len(KEY)]
    return out.hex()


def esc(s: str) -> str:
    r = []
    for c in s:
        if c == "\\":
            r.append("\\\\")
        elif c == "|":
            r.append("\\|")
        else:
            r.append(c)
    return "".join(r)


def user_line(login: str, password: str, full_name: str) -> str:
    inner = "|".join([esc(login), esc(password), esc(full_name)])
    return xor_hex(inner.encode("utf-8"))


def order_line(
    oid: int,
    client: str,
    phone: str,
    desc: str,
    price: float,
    status: str,
    recv: str,
    ded: str,
    master: str,
) -> str:
    ps = f"{price:.2f}"
    parts = [str(oid), client, phone, desc, ps, status, recv, ded, master]
    inner = "|".join(esc(p) for p in parts)
    return xor_hex(inner.encode("utf-8"))


